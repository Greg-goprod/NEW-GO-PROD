// Icone par defaut pour plateformes non configurees
const DEFAULT_ICON = (
  <ExternalLink className="w-4 h-4" />
);

export function ArtistSocialLinks({ artistId }: ArtistSocialLinksProps) {
  const [links, setLinks] = useState<SocialLink[]>([]);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    fetchSocialLinks();
  }, [artistId]);

  const fetchSocialLinks = async () => {
    try {
      const { data, error } = await supabase
        .from('artist_links_songstats')
        .select('source, external_id, url')
        .eq('artist_id', artistId);

      if (error) throw error;
      
      // Trier par priorite
      const sortedLinks = (data || []).sort((a, b) => {
        const priorityA = PLATFORM_CONFIG[a.source]?.priority || 999;
        const priorityB = PLATFORM_CONFIG[b.source]?.priority || 999;
        return priorityA - priorityB;
      });
      
      setLinks(sortedLinks);
    } catch (err) {
      console.error('Error fetching social links:', err);
    } finally {
      setLoading(false);
    }
  };

  if (loading) {
    return (
      <div className="card-surface p-6 rounded-xl">
        <div className="animate-pulse flex gap-3">
          {[...Array(6)].map((_, i) => (
            <div key={i} className="w-10 h-10 bg-slate-200 dark:bg-gray-700 rounded-full" />
          ))}
        </div>
      </div>
    );
  }

  if (links.length === 0) {
    return null;
  }

  return (
    <div className="card-surface p-6 rounded-xl">
      <h3 className="text-sm font-semibold text-slate-600 dark:text-gray-400 uppercase mb-4">
        Plateformes ({links.length})
      </h3>
      <div className="flex flex-wrap gap-3">
        {links.map((link) => {
          const config = PLATFORM_CONFIG[link.source];
          const name = config?.name || link.source;
          const icon = config?.icon || DEFAULT_ICON;
          const bgColor = config?.color || '#6B7280';
          const isGradient = bgColor.includes('gradient');
          
          return (
            <a
              key={link.source}
              href={link.url}
              target="_blank"
              rel="noopener noreferrer"
              className="group relative transition-all hover:scale-110"
              title={name}
            >
              <div 
                className="w-10 h-10 rounded-full flex items-center justify-center text-white shadow-lg"
                style={{ 
                  background: isGradient ? bgColor : undefined,
                  backgroundColor: !isGradient ? bgColor : undefined,
                }}
              >
                {icon}
              </div>
              {/* Tooltip */}
              <div className="absolute -bottom-8 left-1/2 -translate-x-1/2 px-2 py-1 bg-slate-900 dark:bg-gray-800 text-white text-xs rounded opacity-0 group-hover:opacity-100 transition-opacity whitespace-nowrap pointer-events-none z-10">
                {name}
              </div>
            </a>
          );
        })}
      </div>
    </div>
  );
}







